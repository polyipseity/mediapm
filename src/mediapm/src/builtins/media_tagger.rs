/// Runtime options for one internal media-tagger invocation.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct InternalMediaTaggerOptions {
    /// Optional input media path to inspect and tag.
    ///
    /// This is required only for fingerprint-based autodetection paths.
    pub input_path: Option<PathBuf>,
    /// Output `FFmetadata` path generated by this metadata-fetch stage.
    pub output_path: PathBuf,
    /// Optional AcoustID API key override.
    pub acoustid_api_key: Option<String>,
    /// AcoustID lookup endpoint.
    pub acoustid_endpoint: String,
    /// MusicBrainz API endpoint used in diagnostics.
    pub musicbrainz_endpoint: String,
    /// Optional media-tagger HTTP cache root directory.
    ///
    /// When provided, endpoint and cover-art payload responses are cached
    /// through one shared cache layout:
    /// - `<cache_dir>/store/` CAS payload objects,
    /// - `<cache_dir>/media-tagger.jsonc` key-to-hash metadata index.
    ///
    /// This keeps media-tagger cache semantics aligned with the workspace/user
    /// managed cache model and avoids dedicated per-tool directories inside
    /// `store/`.
    pub cache_dir: Option<PathBuf>,
    /// Cache expiry budget in seconds.
    ///
    /// Negative values disable cache expiry and keep rows indefinitely.
    pub cache_expiry_seconds: i64,
    /// Whether unresolved identity should fail the invocation.
    pub strict_identification: bool,
    /// Whether to emit extended `Picard`-compatible tags from available payloads.
    pub write_all_tags: bool,
    /// Whether to enrich metadata with `Picard`-compatible `coverart_*` tags.
    pub write_all_images: bool,
    /// Number of deterministic cover-art slots emitted for downstream apply.
    ///
    /// Managed workflow synthesis binds ffmpeg cover-image inputs to these
    /// slot files, so the internal tagger always writes all slot members:
    /// populated slots carry image bytes and "true" flags, unused slots carry
    /// empty payloads and empty flags.
    pub cover_art_slot_count: usize,
    /// Optional direct recording MBID override.
    pub recording_mbid: Option<String>,
    /// Optional direct release MBID override.
    pub release_mbid: Option<String>,
}

/// Executes one full internal tagging pipeline.
///
/// # Errors
///
/// Returns an error when metadata identification/fetching fails in strict mode,
/// when fallback metadata output cannot be written, or when upstream tool/API
/// calls fail in a way that prevents deterministic output generation.
pub async fn run_internal_media_tagger(options: InternalMediaTaggerOptions) -> anyhow::Result<()> {
    if let Some(input_path) = options.input_path.as_ref()
        && !input_path.exists()
    {
        bail!("input media path '{}' does not exist", input_path.display());
    }

    let output_path = options.output_path.clone();
    let fallback_input = options.input_path.clone();
    match run_internal_media_tagger_impl(options).await {
        Ok(()) => Ok(()),
        Err(error) => {
            let fallback_map = fallback_ffmetadata_map(fallback_input.as_deref()).await;
            if let Err(write_error) = write_ffmetadata_document(&output_path, &fallback_map) {
                return Err(error.context(format!(
                    "failed to persist fallback ffmetadata output '{}' after tagging error: {write_error}",
                    output_path.display()
                )));
            }

            Err(error)
        }
    }
}

/// Executes one full internal tagging pipeline without fallback-output handling.
#[expect(
    clippy::too_many_lines,
    reason = "this item intentionally keeps end-to-end tagging flow explicit for maintainability"
)]
async fn run_internal_media_tagger_impl(options: InternalMediaTaggerOptions) -> anyhow::Result<()> {
    let resolved_input = options
        .input_path
        .as_ref()
        .map(|input_path| input_path.canonicalize().unwrap_or_else(|_| input_path.clone()));
    let media_tagger_cache = MediaTaggerHttpCache::new(
        options.cache_dir.clone(),
        CacheExpiryPolicy::from_seconds(options.cache_expiry_seconds),
    );
    let mut detected_recording_mbid = normalize_optional_text(options.recording_mbid.as_deref());
    let mut detected_release_mbid = normalize_optional_text(options.release_mbid.as_deref());

    if detected_recording_mbid.is_none() {
        let Some(resolved_input) = resolved_input.as_ref() else {
            bail!(
                "input media is required when --recording-mbid is not provided (fingerprint autodetection path)"
            );
        };

        let acoustid_api_key = require_acoustid_api_key_for_lookup(resolve_acoustid_api_key(
            options.acoustid_api_key.as_deref(),
        ))?;
        let fingerprint_payload = decode_and_fingerprint_audio(resolved_input)
            .await
            .context("decoding media for fingerprinting")?;
        let match_result = lookup_acoustid_match(
            &options.acoustid_endpoint,
            &acoustid_api_key,
            &fingerprint_payload.fingerprint,
            fingerprint_payload.duration_seconds,
        )
        .await
        .with_context(|| {
            format!(
                "resolving recording identity through AcoustID endpoint '{}'",
                options.acoustid_endpoint
            )
        })?;

        detected_recording_mbid = detected_recording_mbid.or(match_result.recording_mbid);
        detected_release_mbid = detected_release_mbid.or(match_result.release_mbid);
    }

    let Some(recording_mbid) = detected_recording_mbid else {
        if options.strict_identification {
            bail!(
                "could not resolve recording MBID (set --recording-mbid or provide AcoustID API key via --acoustid-api-key/{ACOUSTID_API_KEY_ENV})"
            );
        }

        let fallback_map = fallback_ffmetadata_map(resolved_input.as_deref()).await;
        write_ffmetadata_document(&options.output_path, &fallback_map)?;
        return Ok(());
    };

    let metadata_payload =
        fetch_musicbrainz_payloads(&recording_mbid, detected_release_mbid.as_deref())
            .await
            .with_context(|| {
                format!(
                    "fetching MusicBrainz entities from '{}' for recording '{}'",
                    options.musicbrainz_endpoint, recording_mbid
                )
            });

    let metadata_payload = match metadata_payload {
        Ok(payload) => payload,
        Err(error) => {
            if options.strict_identification {
                return Err(error);
            }
            let fallback_map = fallback_ffmetadata_map(resolved_input.as_deref()).await;
            write_ffmetadata_document(&options.output_path, &fallback_map)?;
            return Ok(());
        }
    };

    let mut ffmetadata_map = build_ffmetadata_map(&metadata_payload, options.write_all_tags)
        .context("building FFmetadata mapping from MusicBrainz payload")?;

    let mut selected_cover_art = Vec::new();
    if options.write_all_images {
        selected_cover_art = collect_musicbrainz_cover_art(&metadata_payload, &media_tagger_cache)
            .await
            .context("collecting MusicBrainz cover-art entries")?;
        insert_musicbrainz_image_tags(&mut ffmetadata_map, &selected_cover_art);
    }

    if let Some(input_path) = resolved_input.as_deref() {
        let existing_metadata = extract_existing_ffmetadata_map(input_path).await?;
        if !existing_metadata.is_empty() {
            let mut merged = existing_metadata;
            for (key, value) in ffmetadata_map {
                merged.insert(key, value);
            }
            ffmetadata_map = merged;
        }
    }

    if ffmetadata_map.is_empty() {
        if options.strict_identification {
            bail!("resolved metadata payload did not produce any FFmetadata entries");
        }
        let fallback_map = fallback_ffmetadata_map(resolved_input.as_deref()).await;
        write_ffmetadata_document(&options.output_path, &fallback_map)?;
        return Ok(());
    }

    persist_cover_art_slot_artifacts(
        &options.output_path,
        &selected_cover_art,
        options.cover_art_slot_count,
        &media_tagger_cache,
    )
    .await
    .context("writing media-tagger cover-art slot artifacts")?;

    write_ffmetadata_document(&options.output_path, &ffmetadata_map)
}

/// Builds fallback FFmetadata content from existing input media metadata.
///
/// When input media is available, fallback output should preserve existing tags
/// to avoid destructive metadata clears in downstream ffmpeg apply stages.
async fn fallback_ffmetadata_map(input_path: Option<&Path>) -> BTreeMap<String, String> {
    let Some(input_path) = input_path else {
        return BTreeMap::new();
    };

    extract_existing_ffmetadata_map(input_path).await.unwrap_or_default()
}

/// Extracts current global metadata from one media file through ffmpeg
/// FFmetadata export and decodes it into a key/value map.
async fn extract_existing_ffmetadata_map(
    input_path: &Path,
) -> anyhow::Result<BTreeMap<String, String>> {
    let ffmpeg_executable = resolve_ffmpeg_executable();
    let output = Command::new(&ffmpeg_executable)
        .arg("-v")
        .arg("error")
        .arg("-i")
        .arg(input_path)
        // Skip codec initialization: the FFmetadata muxer reads only
        // container-level tags and does not need to decode any streams.
        .arg("-vn")
        .arg("-an")
        .arg("-sn")
        .arg("-dn")
        .arg("-f")
        .arg("ffmetadata")
        .arg("-")
        .output()
        .await
        .with_context(|| {
            format!(
                "running '{ffmpeg_executable}' to extract existing metadata from '{}'",
                input_path.display()
            )
        })?;

    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        bail!(
            "ffmpeg metadata extraction failed for '{}' with status {}: {stderr}",
            input_path.display(),
            output.status
        );
    }

    let text = String::from_utf8(output.stdout).with_context(|| {
        format!(
            "ffmpeg metadata extraction output for '{}' was not valid UTF-8",
            input_path.display()
        )
    })?;

    Ok(parse_ffmetadata_global_map(&text))
}

/// Parses ffmpeg `-f ffmetadata -` output into a global metadata map.
fn parse_ffmetadata_global_map(document: &str) -> BTreeMap<String, String> {
    let mut metadata = BTreeMap::new();
    for raw_line in document.lines() {
        let line = raw_line.trim();
        if line.is_empty() || line.starts_with(';') {
            continue;
        }
        if line.starts_with('[') {
            break;
        }
        let Some((raw_key, raw_value)) = line.split_once('=') else {
            continue;
        };

        let key = decode_ffmetadata_escape(raw_key);
        let value = decode_ffmetadata_escape(raw_value);
        if key.trim().is_empty() || value.trim().is_empty() {
            continue;
        }
        metadata.insert(key, value);
    }

    metadata
}

/// Decodes ffmetadata backslash escapes for one scalar key/value text fragment.
fn decode_ffmetadata_escape(value: &str) -> String {
    let mut decoded = String::new();
    let mut chars = value.chars();
    while let Some(character) = chars.next() {
        if character == '\\' {
            if let Some(next) = chars.next() {
                decoded.push(next);
            }
            continue;
        }
        decoded.push(character);
    }
    decoded
}

/// Decoded fingerprint payload plus AcoustID request duration hint.
#[derive(Debug, Clone, PartialEq, Eq)]
struct FingerprintPayload {
    /// Encoded Chromaprint fingerprint string used by AcoustID lookup.
    fingerprint: String,
    /// Rounded audio duration in whole seconds.
    duration_seconds: u32,
}

/// Resolves AcoustID API key from CLI override or environment.
fn resolve_acoustid_api_key(override_value: Option<&str>) -> Option<String> {
    if let Some(override_value) = override_value {
        if let Some(value) = normalize_optional_text(Some(override_value)) {
            return Some(value);
        }

        // An explicit blank override intentionally disables environment
        // fallback so callers can force the "missing-key" flow.
        return None;
    }

    if let Some(value) = std::env::var_os(ACOUSTID_API_KEY_ENV)
        .and_then(|value| value.into_string().ok())
        .and_then(|value| normalize_optional_text(Some(&value)))
    {
        return Some(value);
    }

    None
}

/// Enforces that AcoustID autodetection has a non-empty API key.
///
/// The lookup path is entered only when callers omit `recording_mbid`.
/// At that point, missing credentials represent a configuration error:
/// runtime would otherwise silently disable autodetection and surprise users.
///
/// In `mediapm sync` executions, the internal media-tagger runs as a managed
/// executable tool under conductor. In that mode, operators typically need to
/// include `ACOUSTID_API_KEY` in `runtime.inherited_env_vars` so the tool
/// subprocess can read it from environment.
fn require_acoustid_api_key_for_lookup(resolved_key: Option<String>) -> anyhow::Result<String> {
    let Some(api_key) = resolved_key else {
        bail!(
            "AcoustID lookup requires a non-empty API key; set --acoustid-api-key or {ACOUSTID_API_KEY_ENV} (for mediapm sync workflows, ensure runtime.inherited_env_vars includes {ACOUSTID_API_KEY_ENV})"
        );
    };

    Ok(api_key)
}

/// Runs `ffmpeg` decode + Chromaprint fingerprint generation for one media file.
async fn decode_and_fingerprint_audio(input_path: &Path) -> anyhow::Result<FingerprintPayload> {
    let ffmpeg_executable = resolve_ffmpeg_executable();
    let output = Command::new(&ffmpeg_executable)
        .arg("-v")
        .arg("error")
        .arg("-i")
        .arg(input_path)
        .arg("-vn")
        .arg("-f")
        .arg("s16le")
        .arg("-ac")
        .arg(FINGERPRINT_CHANNELS.to_string())
        .arg("-ar")
        .arg(FINGERPRINT_SAMPLE_RATE.to_string())
        .arg("-")
        .output()
        .await
        .with_context(|| {
            format!("running '{ffmpeg_executable}' to decode '{}'", input_path.display())
        })?;

    if !output.status.success() {
        let stderr = String::from_utf8_lossy(&output.stderr);
        bail!(
            "ffmpeg decode failed for '{}' with status {}: {stderr}",
            input_path.display(),
            output.status
        );
    }

    if output.stdout.is_empty() {
        bail!("ffmpeg decode for '{}' produced no PCM bytes", input_path.display());
    }

    let samples = output
        .stdout
        .chunks_exact(2)
        .map(|chunk| i16::from_le_bytes([chunk[0], chunk[1]]))
        .collect::<Vec<_>>();

    if samples.is_empty() {
        bail!("decoded PCM sample stream is empty");
    }

    let fingerprint = fingerprint_audio(
        &samples,
        FINGERPRINT_SAMPLE_RATE,
        FINGERPRINT_CHANNELS,
        Algorithm::default(),
    )
    .context("computing chromaprint fingerprint")?;

    let encoded_fingerprint = fingerprint.encoded().to_string();
    let sample_count = u128::try_from(samples.len()).unwrap_or(u128::MAX);
    let samples_per_second = u128::from(FINGERPRINT_SAMPLE_RATE) * u128::from(FINGERPRINT_CHANNELS);
    let rounded_seconds = ((sample_count + (samples_per_second / 2)) / samples_per_second).max(1);
    let duration_seconds = u32::try_from(rounded_seconds).unwrap_or(u32::MAX);

    Ok(FingerprintPayload { fingerprint: encoded_fingerprint, duration_seconds })
}

/// AcoustID API response payload.
#[derive(Debug, Deserialize)]
struct AcoustIdLookupResponse {
    /// API status string (`ok` on success).
    status: String,
    /// Candidate matches returned by AcoustID.
    #[serde(default)]
    results: Vec<AcoustIdResult>,
}

/// Wire-format query parameters for AcoustID lookup.
#[derive(Debug, Serialize)]
struct AcoustIdLookupQuery<'a> {
    /// API key issued by AcoustID.
    client: &'a str,
    /// Requested lookup metadata projections.
    meta: &'a str,
    /// Rounded clip duration in seconds.
    duration: u32,
    /// Encoded Chromaprint fingerprint text.
    fingerprint: &'a str,
    /// Output response format.
    format: &'a str,
}

/// One AcoustID candidate match.
#[derive(Debug, Deserialize)]
struct AcoustIdResult {
    /// Match confidence score in `[0, 1]`.
    score: Option<f64>,
    /// Candidate recordings for this fingerprint match.
    #[serde(default)]
    recordings: Vec<AcoustIdRecording>,
}

/// Recording payload returned in AcoustID lookup metadata.
#[derive(Debug, Deserialize)]
struct AcoustIdRecording {
    /// MusicBrainz recording identifier.
    id: String,
    /// Optional release candidates linked to this recording.
    #[serde(default)]
    releases: Vec<AcoustIdRelease>,
}

/// Release payload returned in AcoustID lookup metadata.
#[derive(Debug, Deserialize)]
struct AcoustIdRelease {
    /// MusicBrainz release identifier.
    id: String,
}

/// Selected AcoustID match values used for downstream fetches.
#[derive(Debug, Clone, PartialEq, Eq, Default)]
struct AcoustIdMatch {
    /// Selected recording MBID, when available.
    recording_mbid: Option<String>,
    /// Selected release MBID, when available.
    release_mbid: Option<String>,
}

/// Calls AcoustID lookup API and returns highest-scoring MBID pair.
async fn lookup_acoustid_match(
    endpoint: &str,
    api_key: &str,
    fingerprint: &str,
    duration_seconds: u32,
) -> anyhow::Result<AcoustIdMatch> {
    let http_client = shared_http_client()
        .map_err(|error| anyhow::anyhow!("initializing shared HTTP client failed: {error}"))?;

    let payload = http_client
        .get(endpoint)
        .query(&AcoustIdLookupQuery {
            client: api_key,
            meta: "recordings releases releasegroups tracks compress usermeta sources",
            duration: duration_seconds,
            fingerprint,
            format: "json",
        })
        .send()
        .await
        .context("sending AcoustID lookup request")?
        .error_for_status()
        .context("AcoustID lookup returned non-success status")?
        .text()
        .await
        .context("reading AcoustID lookup response body")?;

    let response = serde_json::from_str::<AcoustIdLookupResponse>(&payload)
        .context("decoding AcoustID lookup response JSON")?;

    if !response.status.eq_ignore_ascii_case("ok") {
        bail!("AcoustID lookup returned status '{}'", response.status);
    }

    let mut ordered_results = response.results;
    ordered_results.sort_by(|left, right| {
        right
            .score
            .unwrap_or_default()
            .partial_cmp(&left.score.unwrap_or_default())
            .unwrap_or(std::cmp::Ordering::Equal)
    });

    let Some(best) = ordered_results.into_iter().next() else {
        return Ok(AcoustIdMatch::default());
    };

    let Some(recording) = best.recordings.into_iter().find(|recording| !recording.id.is_empty())
    else {
        return Ok(AcoustIdMatch::default());
    };

    let release_mbid: Option<String> = recording
        .releases
        .into_iter()
        .map(|release| release.id)
        .find(|value| !value.trim().is_empty());

    Ok(AcoustIdMatch { recording_mbid: Some(recording.id), release_mbid })
}

/// Recording + optional release payload fetched from MusicBrainz.
#[derive(Debug, Clone)]
struct MusicBrainzPayload {
    /// Recording entity resolved from MusicBrainz API.
    recording: Recording,
    /// Optional release entity resolved from MusicBrainz API.
    release: Option<Release>,
}

/// Fetches recording/release payloads through `musicbrainz_rs`.
async fn fetch_musicbrainz_payloads(
    recording_mbid: &str,
    release_mbid: Option<&str>,
) -> anyhow::Result<MusicBrainzPayload> {
    let recording = Recording::fetch()
        .id(recording_mbid)
        .with_artists()
        .with_releases()
        .with_isrcs()
        .with_genres()
        .with_tags()
        .with_annotations()
        .with_work_level_relations()
        .execute()
        .await
        .with_context(|| format!("fetching MusicBrainz recording '{recording_mbid}'"))?;

    let release_id = release_mbid.map(ToOwned::to_owned).or_else(|| {
        recording
            .releases
            .as_ref()
            .and_then(|releases| releases.first())
            .map(|release| release.id.clone())
    });

    let release = if let Some(release_id) = release_id {
        Some(
            Release::fetch()
                .id(&release_id)
                .with_artists()
                .with_recordings()
                .with_release_groups()
                .with_media()
                .with_labels()
                .with_isrcs()
                .with_genres()
                .with_tags()
                .with_annotations()
                .with_recording_level_relations()
                .with_release_group_level_relations()
                .execute()
                .await
                .with_context(|| format!("fetching MusicBrainz release '{release_id}'"))?,
        )
    } else {
        None
    };

    Ok(MusicBrainzPayload { recording, release })
}

/// Converts MusicBrainz payload into FFmetadata key/value map.
fn build_ffmetadata_map(
    payload: &MusicBrainzPayload,
    write_all_tags: bool,
) -> anyhow::Result<BTreeMap<String, String>> {
    let mut tags = BTreeMap::new();

    tags.insert("title".to_string(), payload.recording.title.clone());
    tags.insert("musicbrainz_recordingid".to_string(), payload.recording.id.clone());
    tags.insert("musicbrainz_trackid".to_string(), payload.recording.id.clone());

    if let Some(artist) = artist_credit_text(payload.recording.artist_credit.as_deref()) {
        tags.insert("artist".to_string(), artist);
    }

    if let Some(first_release_date) = payload.recording.first_release_date.as_ref() {
        tags.insert("originaldate".to_string(), first_release_date.0.clone());
    }

    if let Some(isrcs) = payload.recording.isrcs.as_ref()
        && !isrcs.is_empty()
    {
        tags.insert("isrc".to_string(), join_unique(isrcs.iter().map(String::as_str)));
    }

    if let Some(genres) = payload.recording.genres.as_ref() {
        let genre_values = genres.iter().map(|genre| genre.name.as_str()).collect::<Vec<_>>();
        if !genre_values.is_empty() {
            tags.insert("genre".to_string(), join_unique(genre_values));
        }
    }

    if let Some(release) = payload.release.as_ref() {
        tags.insert("album".to_string(), release.title.clone());
        tags.insert("musicbrainz_albumid".to_string(), release.id.clone());

        if let Some(date) = release.date.as_ref() {
            tags.insert("date".to_string(), date.0.clone());
        }
        if let Some(barcode) = release.barcode.as_ref()
            && !barcode.trim().is_empty()
        {
            tags.insert("barcode".to_string(), barcode.clone());
        }
        if let Some(country) = release.country.as_ref()
            && !country.trim().is_empty()
        {
            tags.insert("releasecountry".to_string(), country.clone());
        }
        if let Some(label_info) = release.label_info.as_ref() {
            let labels = label_info
                .iter()
                .filter_map(|info| info.label.as_ref().map(|label| label.name.as_str()))
                .collect::<Vec<_>>();
            if !labels.is_empty() {
                tags.insert("label".to_string(), join_unique(labels));
            }

            let catalog_numbers = label_info
                .iter()
                .filter_map(|info| info.catalog_number.as_deref())
                .collect::<Vec<_>>();
            if !catalog_numbers.is_empty() {
                tags.insert("catalognumber".to_string(), join_unique(catalog_numbers));
            }
        }

        if let Some(release_group) = release.release_group.as_ref() {
            tags.insert("musicbrainz_releasegroupid".to_string(), release_group.id.clone());
            tags.insert("releasegroup".to_string(), release_group.title.clone());
        }

        if let Some(release_artist) = artist_credit_text(release.artist_credit.as_deref()) {
            tags.insert("albumartist".to_string(), release_artist);
        }

        if let Some(track_info) = resolve_track_position(&payload.recording.id, release) {
            tags.insert("tracknumber".to_string(), track_info.track_number);
            tags.insert("discnumber".to_string(), track_info.disc_number.to_string());
            if let Some(total_tracks) = track_info.total_tracks {
                tags.insert("tracktotal".to_string(), total_tracks.to_string());
                tags.insert("totaltracks".to_string(), total_tracks.to_string());
            }
            if let Some(total_discs) = track_info.total_discs {
                tags.insert("disctotal".to_string(), total_discs.to_string());
                tags.insert("totaldiscs".to_string(), total_discs.to_string());
            }
        }
    }

    if write_all_tags {
        let mut recording_flattened = BTreeMap::new();
        flatten_entity_json(
            "musicbrainz_recording",
            &serde_json::to_value(&payload.recording)?,
            &mut recording_flattened,
        );
        let mut release_flattened = BTreeMap::new();
        if let Some(release) = payload.release.as_ref() {
            flatten_entity_json(
                "musicbrainz_release",
                &serde_json::to_value(release)?,
                &mut release_flattened,
            );
        }

        insert_extended_picard_tags(&mut tags, &recording_flattened, &release_flattened);
    }

    Ok(tags)
}

/// Inserts optional Picard-compatible tags from flattened MusicBrainz payloads.
///
/// This helper intentionally emits only known Picard tag keys and rejects the
/// previous broad flatten/alias expansion to avoid non-Picard metadata keys.
fn insert_extended_picard_tags(
    tags: &mut BTreeMap<String, String>,
    recording_flattened: &BTreeMap<String, String>,
    release_flattened: &BTreeMap<String, String>,
) {
    for (alias, source_key) in [
        ("albumartistsort", "musicbrainz_release_artist_credit_0_artist_sort_name"),
        ("artistsort", "musicbrainz_recording_artist_credit_0_artist_sort_name"),
        ("artistcredit", "artist"),
        ("albumartistcredit", "albumartist"),
        ("releasestatus", "musicbrainz_release_status"),
        ("releasetype", "musicbrainz_release_release_group_primary_type"),
        ("secondaryreleasetype", "musicbrainz_release_release_group_secondary_types"),
        ("primaryreleasetype", "musicbrainz_release_release_group_primary_type"),
        ("releasegroup", "musicbrainz_release_release_group_title"),
        ("musicbrainz_releasegroupid", "musicbrainz_release_release_group_id"),
        ("musicbrainz_artistid", "musicbrainz_recording_artist_credit_0_artist_id"),
        ("musicbrainz_albumartistid", "musicbrainz_release_artist_credit_0_artist_id"),
        ("recordingtitle", "musicbrainz_recording_title"),
        ("recordingcomment", "musicbrainz_recording_disambiguation"),
        ("releasecomment", "musicbrainz_release_disambiguation"),
        ("releasegroupcomment", "musicbrainz_release_release_group_disambiguation"),
        ("releaselanguage", "musicbrainz_release_text_representation_language"),
        ("script", "musicbrainz_release_text_representation_script"),
        ("releaseannotation", "musicbrainz_release_annotation"),
        ("recordingannotation", "musicbrainz_recording_annotation"),
        ("releasedate", "date"),
        ("releasecountry", "musicbrainz_release_country"),
        ("media", "musicbrainz_release_media_0_format"),
        ("asin", "musicbrainz_release_asin"),
    ] {
        if let Some(value) = tags.get(source_key).cloned().or_else(|| {
            recording_flattened
                .get(source_key)
                .cloned()
                .or_else(|| release_flattened.get(source_key).cloned())
        }) {
            insert_tag_if_absent(tags, alias, &value);
        }
    }
}

/// Inserts one normalized tag key/value when key is absent and value is non-empty.
fn insert_tag_if_absent(tags: &mut BTreeMap<String, String>, key: &str, value: &str) {
    if value.trim().is_empty() {
        return;
    }
    tags.entry(sanitize_metadata_key(key)).or_insert_with(|| truncate_metadata_value(value));
}

