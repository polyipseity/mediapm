/// Emits deterministic image-link metadata tags.
///
/// Keep the emitted `coverart_*` key family synchronized with Picard's
/// cover-art metadata usage in
/// `https://github.com/metabrainz/picard/blob/master/picard/coverart/image.py`.
fn insert_musicbrainz_image_tags(tags: &mut BTreeMap<String, String>, images: &[SelectedCoverArt]) {
    if images.is_empty() {
        return;
    }

    let primary = images.iter().find(|image| image.maintype == "front").unwrap_or(&images[0]);

    tags.insert("coverart_url".to_string(), primary.url.clone());
    for (index, image) in images.iter().enumerate() {
        tags.insert(format!("coverart_url_{index}"), image.url.clone());
    }
    tags.insert("coverart_maintype".to_string(), primary.maintype.clone());

    let all_types =
        join_unique(images.iter().flat_map(|image| image.types.iter().map(String::as_str)));
    if !all_types.trim().is_empty() {
        tags.insert("coverart_types".to_string(), all_types);
    }

    tags.insert("coverart_comment".to_string(), primary.comment.clone());
}

/// Writes deterministic cover-art slot members consumed by apply-stage ffmpeg.
async fn persist_cover_art_slot_artifacts(
    output_path: &Path,
    selected_cover_art: &[SelectedCoverArt],
    slot_count: usize,
    cache: &MediaTaggerHttpCache,
) -> anyhow::Result<()> {
    let artifact_directory = resolve_cover_art_artifact_directory(output_path);
    fs::create_dir_all(&artifact_directory).with_context(|| {
        format!(
            "creating media-tagger cover-art artifact directory '{}'",
            artifact_directory.display()
        )
    })?;

    if selected_cover_art.len() > slot_count {
        bail!(
            "media-tagger resolved {} cover-art entries but only {slot_count} slot(s) are available; increase tools.ffmpeg.max_input_slots to at least {}",
            selected_cover_art.len(),
            selected_cover_art.len() + 1,
        );
    }

    let http_client = shared_http_client()
        .map_err(|error| anyhow::anyhow!("initializing shared HTTP client failed: {error}"))?;

    let mut downloaded = Vec::new();
    for image in selected_cover_art {
        let Some(bytes) = download_cover_art_bytes(http_client, &image.url, cache)
            .await
            .with_context(|| format!("downloading cover-art payload '{}'", image.url))?
        else {
            continue;
        };

        downloaded.push(bytes);
    }

    if downloaded.len() > slot_count {
        bail!(
            "downloaded {} cover-art entries but only {slot_count} slot(s) are available",
            downloaded.len()
        );
    }

    for slot_index in 1..=slot_count {
        let image_member = cover_art_slot_image_member_name(slot_index);
        let flag_member = cover_art_slot_flag_member_name(slot_index);
        let image_path = artifact_directory.join(&image_member);
        let flag_path = artifact_directory.join(&flag_member);

        if let Some(bytes) = downloaded.get(slot_index - 1) {
            fs::write(&image_path, bytes).with_context(|| {
                format!("writing cover-art slot payload '{}'", image_path.display())
            })?;
            fs::write(&flag_path, b"true").with_context(|| {
                format!("writing cover-art slot flag '{}'", flag_path.display())
            })?;
        } else {
            fs::write(&image_path, &[] as &[u8]).with_context(|| {
                format!("writing empty cover-art slot payload '{}'", image_path.display())
            })?;
            fs::write(&flag_path, &[] as &[u8]).with_context(|| {
                format!("writing empty cover-art slot flag '{}'", flag_path.display())
            })?;
        }
    }

    Ok(())
}

/// Resolves sandbox folder where managed runs expose materialized input files.
fn resolve_cover_art_artifact_directory(output_path: &Path) -> PathBuf {
    let metadata_dir = output_path.parent().unwrap_or_else(|| Path::new("."));
    // Cover art files are placed under a dedicated `coverart/` directory in the
    // sandbox root rather than alongside the input media in `inputs/`. This keeps
    // cover art separated from the large input media file and ensures the
    // `sandbox_artifacts` capture for media-tagger can target `coverart/` only,
    // avoiding redundant capture of `inputs/input.media`.
    metadata_dir
        .parent()
        .map_or_else(|| metadata_dir.join("coverart"), |sandbox_root| sandbox_root.join("coverart"))
}

/// Downloads one cover-art payload, returning `None` when URL is unreachable.
async fn download_cover_art_bytes(
    http_client: &reqwest::Client,
    url: &str,
    cache: &MediaTaggerHttpCache,
) -> anyhow::Result<Option<Vec<u8>>> {
    let cached_bytes = cache.read_cover_art_bytes(url).await;
    if let Some(cached) = cached_bytes.as_ref()
        && cached.is_fresh
    {
        return Ok((!cached.payload.is_empty()).then(|| cached.payload.clone()));
    }

    let stale_cached_payload = cached_bytes
        .as_ref()
        .and_then(|cached| (!cached.payload.is_empty()).then(|| cached.payload.clone()));

    let Ok(response) = http_get_with_retry(http_client, url).await else {
        return Ok(stale_cached_payload);
    };

    let status = response.status();
    if status == reqwest::StatusCode::NOT_FOUND {
        return Ok(None);
    }

    if status.is_server_error() {
        return Ok(stale_cached_payload);
    }

    if !status.is_success() {
        return Ok(stale_cached_payload);
    }

    let bytes = response
        .bytes()
        .await
        .with_context(|| format!("reading cover-art payload bytes from '{url}'"))?;

    if bytes.is_empty() {
        return Ok(stale_cached_payload);
    }

    let payload = bytes.to_vec();
    let _ = cache.write_cover_art_bytes(url, &payload).await;
    Ok(Some(payload))
}

/// Track-position metadata resolved from release media structure.
#[derive(Debug, Clone, PartialEq, Eq)]
struct TrackPosition {
    /// Human-facing track number from the release tracklist.
    track_number: String,
    /// One-based disc index in release media ordering.
    disc_number: u32,
    /// Optional total track count for the current disc.
    total_tracks: Option<u32>,
    /// Optional total disc count for the release.
    total_discs: Option<u32>,
}

/// Resolves track/disc position metadata by matching recording MBID.
fn resolve_track_position(recording_mbid: &str, release: &Release) -> Option<TrackPosition> {
    let medias = release.media.as_ref()?;

    for media in medias {
        let tracks = media.tracks.as_ref()?;
        for track in tracks {
            let Some(recording) = track.recording.as_ref() else {
                continue;
            };
            if recording.id != recording_mbid {
                continue;
            }

            return Some(TrackPosition {
                track_number: track.number.clone(),
                disc_number: media.position.unwrap_or(1),
                total_tracks: Some(media.track_count),
                total_discs: media.disc_count,
            });
        }
    }

    None
}

/// Converts artist-credit list into stable display string.
fn artist_credit_text(
    artist_credit: Option<&[musicbrainz_rs::entity::artist_credit::ArtistCredit]>,
) -> Option<String> {
    let artist_credit = artist_credit?;
    if artist_credit.is_empty() {
        return None;
    }

    let mut combined = String::new();
    for credit in artist_credit {
        combined.push_str(&credit.name);
        if let Some(join_phrase) = credit.joinphrase.as_deref() {
            combined.push_str(join_phrase);
        }
    }

    if combined.trim().is_empty() { None } else { Some(combined) }
}

/// Joins string values while preserving first-seen order and uniqueness.
fn join_unique<'a>(values: impl IntoIterator<Item = &'a str>) -> String {
    let mut seen = BTreeSet::new();
    let mut ordered = Vec::new();

    for value in values {
        let trimmed = value.trim();
        if trimmed.is_empty() {
            continue;
        }
        if seen.insert(trimmed.to_string()) {
            ordered.push(trimmed.to_string());
        }
    }

    ordered.join("; ")
}

/// Flattens one arbitrary JSON tree into FFmetadata-safe key/value pairs.
fn flatten_entity_json(prefix: &str, value: &Value, out: &mut BTreeMap<String, String>) {
    fn walk(prefix: &str, value: &Value, out: &mut BTreeMap<String, String>) {
        if out.len() >= MAX_FLATTENED_METADATA_ENTRIES {
            return;
        }

        match value {
            Value::Object(map) => {
                let mut entries = map.iter().collect::<Vec<_>>();
                entries.sort_by(|(left, _), (right, _)| left.cmp(right));
                for (key, nested) in entries {
                    let nested_prefix = format!("{prefix}_{key}");
                    walk(&nested_prefix, nested, out);
                }
            }
            Value::Array(items) => {
                if items.iter().all(is_scalar_json_value) {
                    let joined = items
                        .iter()
                        .filter_map(json_scalar_to_string)
                        .collect::<Vec<_>>()
                        .join("; ");
                    let key = sanitize_metadata_key(prefix);
                    if !joined.is_empty() {
                        out.insert(key, truncate_metadata_value(&joined));
                    }
                } else {
                    for (index, nested) in items.iter().enumerate() {
                        walk(&format!("{prefix}_{index}"), nested, out);
                    }
                }
            }
            _ => {
                if let Some(rendered) = json_scalar_to_string(value)
                    && !rendered.is_empty()
                {
                    out.insert(sanitize_metadata_key(prefix), truncate_metadata_value(&rendered));
                }
            }
        }
    }

    walk(prefix, value, out);
}

/// Returns true when one JSON value can be represented as scalar metadata text.
fn is_scalar_json_value(value: &Value) -> bool {
    matches!(value, Value::Null | Value::Bool(_) | Value::Number(_) | Value::String(_))
}

/// Converts one scalar JSON value into metadata text.
fn json_scalar_to_string(value: &Value) -> Option<String> {
    match value {
        Value::Bool(flag) => Some(flag.to_string()),
        Value::Number(number) => Some(number.to_string()),
        Value::String(text) => normalize_optional_text(Some(text)),
        _ => None,
    }
}

/// Sanitizes one metadata key for broad ffmpeg tag compatibility.
fn sanitize_metadata_key(input: &str) -> String {
    let mut key =
        input
            .chars()
            .map(|character| {
                if character.is_ascii_alphanumeric() { character.to_ascii_lowercase() } else { '_' }
            })
            .collect::<String>();

    while key.contains("__") {
        key = key.replace("__", "_");
    }
    key = key.trim_matches('_').to_string();

    if key.is_empty() {
        return "metadata".to_string();
    }
    if key.starts_with(|character: char| character.is_ascii_digit()) {
        return format!("meta_{key}");
    }

    key
}

/// Truncates one metadata value to bounded maximum length.
fn truncate_metadata_value(value: &str) -> String {
    if value.len() <= MAX_FLATTENED_VALUE_LEN {
        value.to_string()
    } else {
        value.chars().take(MAX_FLATTENED_VALUE_LEN).collect::<String>()
    }
}

/// Persists one FFmetadata document to requested output path.
fn write_ffmetadata_document(
    output_path: &Path,
    metadata_map: &BTreeMap<String, String>,
) -> anyhow::Result<()> {
    let document = FFMetadata {
        global: metadata_map
            .iter()
            .map(|(key, value)| (key.clone(), value.clone()))
            .collect::<Vec<_>>(),
        ..FFMetadata::default()
    };

    if let Some(parent) = output_path.parent() {
        fs::create_dir_all(parent).with_context(|| {
            format!("creating ffmetadata output directory '{}'", parent.display())
        })?;
    }

    fs::write(output_path, document.to_string())
        .with_context(|| format!("writing ffmetadata file '{}'", output_path.display()))?;

    Ok(())
}

/// Returns normalized, non-empty optional string value.
fn normalize_optional_text(value: Option<&str>) -> Option<String> {
    value.map(str::trim).filter(|value| !value.is_empty()).map(ToString::to_string)
}

/// Resolves ffmpeg executable path for internal tagger operations.
///
/// Managed sync injects a host-specific relative executable path through
/// `MEDIA_TAGGER_FFMPEG_BIN_ENV` so internal launchers can run without relying
/// on ambient PATH state.
fn resolve_ffmpeg_executable() -> String {
    normalize_optional_text(std::env::var(MEDIA_TAGGER_FFMPEG_BIN_ENV).ok().as_deref())
        .unwrap_or_else(|| FFMPEG_EXECUTABLE.to_string())
}

