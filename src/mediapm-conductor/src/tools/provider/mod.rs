/// Fetches (or generates) bytes for every source in `fetch`.
///
/// URL sources are tried in order; the first HTTP 200 wins. Launcher sources
/// generate shell/batch script bytes in memory. Uses `cache` for deduplication
/// of downloaded payloads.
///
/// Progress reporting: the per-chunk total combines already-completed sizes
/// (`agg_completed_bytes`) + the best lower bound for the current source
/// (Content-Length if available, otherwise `downloaded_so_far`) + expected
/// sizes for unstarted sources (`remaining_expected`).  After download,
/// [`DownloadedSource.expected_size`] is set to the actual byte count
/// (or `max(expected, actual)` if a HEAD-based estimate existed).
///
/// # Errors
///
/// Returns [`ConductorError`] when all URL candidates fail or I/O fails.
#[cfg(feature = "tool-presets")]
pub async fn fetch_tool_sources(
    fetch: &ResolvedToolFetch,
    cache: &crate::cache_user_level::UserLevelCache,
    progress_cb: Option<ProviderProgressCallback>,
) -> Result<DownloadedSources, crate::error::ConductorError> {
    let mut entries = Vec::with_capacity(fetch.sources.len());
    let mut cached_count: usize = 0;
    let total = fetch.sources.len() as u64;
    let mut agg_completed_bytes: u64 = 0;

    // Pre-compute suffix sums of expected_size: suffix_expected[i] = sum of
    // expected_size for sources[i..].  remaining_expected for sources after
    // the current one is suffix_expected[idx + 1].
    let suffix_expected: Vec<u64> = {
        let n = fetch.sources.len();
        let mut s = vec![0u64; n + 1];
        for (i, src) in fetch.sources.iter().enumerate().rev() {
            s[i] = s[i + 1] + src.expected_size.unwrap_or(0);
        }
        s
    };

    for (idx, source) in fetch.sources.iter().enumerate() {
        let remaining_expected = suffix_expected[idx + 1];
        match &source.producer {
            SourceProducer::Fetch { urls } => {
                let cache_key = &urls[0];
                let bytes = if let Some(cached) = cache.lookup_bytes(cache_key).await {
                    cache.touch(cache_key);
                    agg_completed_bytes += cached.len() as u64;
                    cached_count += 1;
                    cached
                } else {
                    let downloaded = fetch_bytes_from_candidates(
                        urls,
                        &fetch.tool_id,
                        &source.os,
                        &progress_cb,
                        idx as u64,
                        total,
                        agg_completed_bytes,
                        remaining_expected,
                    )
                    .await?;
                    agg_completed_bytes += downloaded.len() as u64;
                    cache.store_bytes(cache_key, &downloaded).await;
                    downloaded
                };
                entries.push(DownloadedSource {
                    os: source.os.clone(),
                    producer: SourceProducer::Fetch { urls: urls.clone() },
                    expected_size: {
                        let actual_size = bytes.len() as u64;
                        source.expected_size.map(|s| s.max(actual_size)).or(Some(actual_size))
                    },
                    bytes,
                });
            }
            SourceProducer::GenerateLauncher { builtin_id } => {
                let bytes = generate_launcher_script(source.os.as_str(), builtin_id);
                agg_completed_bytes += bytes.len() as u64;
                entries.push(DownloadedSource {
                    os: source.os.clone(),
                    producer: SourceProducer::GenerateLauncher { builtin_id: builtin_id.clone() },
                    bytes,
                    expected_size: None,
                });
            }
        }
        if let Some(cb) = progress_cb.as_ref() {
            cb(ProviderProgressSnapshot {
                phase: ProviderPhase::Fetch,
                items: ((idx + 1) as u64, total),
                bytes: (agg_completed_bytes, agg_completed_bytes + remaining_expected),
            });
        }
    }

    Ok(DownloadedSources { tool_id: fetch.tool_id.clone(), entries, cached_count })
}

/// Downloads bytes from URL candidates (tried in order).
#[cfg(feature = "tool-presets")]
async fn fetch_bytes_from_candidates(
    urls: &[String],
    tool_id: &str,
    os_label: &str,
    progress_cb: &Option<ProviderProgressCallback>,
    items_done: u64,
    total: u64,
    agg_completed_bytes: u64,
    remaining_expected: u64,
) -> Result<Vec<u8>, crate::error::ConductorError> {
    use crate::error::ConductorError;
    use futures_util::StreamExt;

    const CONDUCTOR_USER_AGENT: &str = concat!("mediapm-conductor/", env!("CARGO_PKG_VERSION"));

    let client = reqwest::Client::builder()
        .user_agent(CONDUCTOR_USER_AGENT)
        .build()
        .map_err(|e| ConductorError::Workflow(format!("building HTTP client failed: {e}")))?;

    for url in urls {
        let request = client.get(url);
        match request.send().await {
            Ok(response) if response.status().is_success() => {
                let total_bytes = response.content_length();
                let mut downloaded = 0u64;
                let mut buffer = Vec::new();
                let mut stream = response.bytes_stream();
                while let Some(chunk_result) = stream.next().await {
                    let chunk = chunk_result
                        .map_err(|e| ConductorError::Workflow(format!("download error: {e}")))?;
                    downloaded += chunk.len() as u64;
                    buffer.extend_from_slice(&chunk);
                    if let Some(cb) = progress_cb {
                        let bytes_completed = agg_completed_bytes + downloaded;
                        // Best lower bound for current source: Content-Length
                        // if available, otherwise bytes received so far.
                        let current_estimate = total_bytes.unwrap_or(downloaded);
                        // Total = completed sizes + current source bound + expected future.
                        let total_estimate =
                            agg_completed_bytes + current_estimate + remaining_expected;
                        cb(ProviderProgressSnapshot {
                            phase: ProviderPhase::Fetch,
                            items: (items_done, total),
                            bytes: (bytes_completed, total_estimate),
                        });
                    }
                }
                return Ok(buffer);
            }
            Ok(response) => {
                tracing::warn!("HTTP {} for {}, skipping", response.status(), url);
            }
            Err(e) => {
                tracing::warn!("HTTP error for {url}: {e}, skipping");
            }
        }
    }

    Err(ConductorError::Workflow(format!(
        "tool {tool_id} os {os_label}: all {} download candidates failed",
        urls.len()
    )))
}

/// Generates a launcher script that dispatches `$MEDIAPM_EXECUTABLE {builtin_id} {args...}`.
#[cfg(feature = "tool-presets")]
fn generate_launcher_script(os: &str, builtin_id: &str) -> Vec<u8> {
    match os {
        "windows" => {
            format!("@echo off\r\n\"%MEDIAPM_EXECUTABLE%\" {builtin_id} %*\r\n").into_bytes()
        }
        _ => {
            // Linux / macOS — POSIX shell with `exec`.
            format!("#!/bin/sh\nexec \"${{MEDIAPM_EXECUTABLE}}\" {builtin_id} \"$@\"\n")
                .into_bytes()
        }
    }
}

